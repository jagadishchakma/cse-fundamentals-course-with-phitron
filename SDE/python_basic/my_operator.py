# python operators
# Arithmetic Operators
# addition
num1 = 3
num2 = 4
addition = num1 + num2
print(addition)

# subtraction
subtraction = num1 - num2
print(subtraction)

# multiplication
multiplication = num1 * num2
print(multiplication)

# exponentiation
exponentiation = num1 ** num2
print(exponentiation)

# division
division = num2 / num1
print(division)

# floor division
floor_division = num2 // num1
print(floor_division)

# modulus
modulus = num2 % num1
print(modulus)
