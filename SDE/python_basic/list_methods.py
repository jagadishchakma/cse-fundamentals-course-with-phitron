# python tuple data type examples
letters = ("a","b","c","d","e","f")
print(type(letters))

# python range data type
zeroToTen = range(10)
print(zeroToTen)
print(type(zeroToTen))

# python frozenset data types
friends  = ({"apple","banana","chery"})
print(friends)
print(type(friends))

# python boolean data types
agree = True
print(agree)
print(type(agree))

# python none data types
exists = None
print(exists)
print(type(exists))

# python dict data type
person = {"uanme":"Jagadish Chakma", "age": 28}
print(person)
print(type(person))

# python set data types
fruites = {"banana","apple","orange","pineapple"}
print(fruites)
print(type(fruites))

# python data type set custom

nums = list((1,2,3,4))
